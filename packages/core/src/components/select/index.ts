export interface SelectConfig {
  /**
   * The size of the select
   * @default "md"
   */
  size?: 'sm' | 'md' | 'lg';
  /**
   * Whether the select has an error state
   * @default false
   */
  error?: boolean;
  /**
   * Whether the select is disabled
   * @default false
   */
  disabled?: boolean;
}
