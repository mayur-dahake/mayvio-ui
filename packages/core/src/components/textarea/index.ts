export interface TextareaConfig {
  /**
   * Whether the textarea is disabled
   * @default false
   */
  disabled?: boolean;
  
  /**
   * Whether the textarea is in an error state
   * @default false
   */
  error?: boolean;
  
  /**
   * Whether the textarea should automatically resize its height
   * @default false
   */
  autoResize?: boolean;
}
